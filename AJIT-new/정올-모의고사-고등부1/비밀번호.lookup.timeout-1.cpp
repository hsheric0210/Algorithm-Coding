int popcnt_impl(ll a) // QWORD -> DWORD * 2 -> WORD * 4 -> BYTE * 8
{
	int c = 0;
	for (j = 0; j < 8; j++)
		c += lookup[(a >> (j * 8)) & 255];
	return c;
}
int main()
{
	cin >> a;
	y = popcnt_impl(a);
	for (x = a-1;x>0; x--)
	{
		if (popcnt_impl(x) == y)
		{
			b = x;
			break;
		}
	}
	for (x = a+1;; x++)
		if (popcnt_impl(x) == y)
		{
			c = x;
			break;
		}
	cout << b << ' ' << c << '\n';
	return 0;
}